export function getScientistById(id: string): Scientist | undefined {
  return scientists.find((s) => s.id === id);
}

export function getDiscoveryById(id: string): DiscoveryEvent | undefined {
  return discoveryEvents.find((e) => e.id === id);
}

export function getDiscoveriesByEra(era: DiscoveryEvent['era']): DiscoveryEvent[] {
  return discoveryEvents.filter((e) => e.era === era).sort((a, b) => a.year - b.year);
}

export function getAllDiscoveriesSorted(): DiscoveryEvent[] {
  return [...discoveryEvents].sort((a, b) => a.year - b.year);
}

export function getDiscoveriesByCategory(
  category: DiscoveryEvent['category']
): DiscoveryEvent[] {
  return discoveryEvents.filter((e) => e.category === category).sort((a, b) => a.year - b.year);
}
